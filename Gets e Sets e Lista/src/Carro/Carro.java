package Carro;

public class Carro {
	
	private int porta;
	private boolean paradocarro;
	private String cor;
	
	public int getPorta() {
		return porta;
	}
	public void setPorta(int porta) {
		this.porta = porta;
	}
	public boolean isParadocarro() {
		return paradocarro;
	}
	public void setParadocarro(boolean paradocarro) {
		this.paradocarro = paradocarro;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}

}
